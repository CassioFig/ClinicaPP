    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       FuncionarioControl fc = new FuncionarioControl();
       FuncionarioDB fdb = new FuncionarioDB();
       
       int matricula = Integer.parseInt(txtMatricula.getText());
       String nome = String.valueOf(txtNome.getText());
       String endereco = String.valueOf(txtEndereco.getText());
       String numero = String.valueOf(txtNumero.getText());
       String cep = String.valueOf(txtCep.getText());
       String rg = String.valueOf(txtRg.getText());
       String complemento = String.valueOf(txtComplemento.getText());
       String cpf = String.valueOf(txtCpf.getText());
       String sexo = opcSexo();
       
        if (txtMatricula.getText().isBlank() || txtNome.getText().isBlank() ||
                txtEndereco.getText().isBlank() || txtNumero.getText().isBlank()
                || txtCep.getText().isBlank() || txtRg.getText().isBlank() ||
                txtCpf.getText().isBlank() || opcSexo() == null) {
            
            JOptionPane.showMessageDialog(null, "Preencha todos os campos "
                    + "obrigatórios!");
        } else if (consultar() == true) {
            
            JOptionPane.showMessageDialog(null, "Número de matrícula já "
                    + "cadastrada!");
            
        } else{
            fc.setMatricula(matricula);
            fc.setNome(nome);
            fc.setEndereco(endereco);
            fc.setNumero(numero);
            fc.setCep(cep);
            fc.setRg(rg);
            fc.setComplemento(complemento);
            fc.setCpf(cpf);
            fc.setSexo(sexo);
            
            FuncionarioModel fm = fc.cadastrar();
            
            
            fdb.cadastrarFuncionario(fm);
            limpar();
        }
       
    }//GEN-LAST:event_btnCadastrarActionPerformed


