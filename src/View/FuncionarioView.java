    private String opcSexo(){
        String sexo = null;
        
        if (btnGroup.getSelection() == rBtnMasculino.getModel()) {
            sexo = "Masculino";
        } else if (btnGroup.getSelection() == rBtnFeminino.getModel()) {
            sexo = "Feminino";
        } else if (btnGroup.getSelection() == rBtnOutro.getModel()){
            sexo = "Outros";
        }
        
        return sexo;
    }
    
    private boolean consultar(){
        String sql = "select * from tbfuncionario where matricula=?";
        
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtMatricula.getText());
            rs = pst.executeQuery();
            
            if (rs.next()) {
                return true;
            } else if (txtMatricula.getText().isBlank()){
                JOptionPane.showMessageDialog(null, "Digite uma matrícula");
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        return false;
    }
    
